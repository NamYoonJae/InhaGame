#include <conio.h>

//교재 75p
int main(void) {


	printf("%c\n", 'A'); //문자 상수를 ASCII 코드 문자형식으로 출력
	printf("%c\n", 'A' + 1); //문자 'A' 의 ASCII 코드 값 65 에서 1 더한 값 출력
	printf("%c\n", 'C'); //문자 상수를 ASCII 코드 문자형식으로 출력


	printf("%d\n", 'A');//문자 'A'의 ASCII 코드 값을 10진수로 출력
	printf("%d\n", 'A' + 1); //문자 'A'의 ASCII 코드 값에서 1더한 값을 10진수로 출력
	printf("%d\n", 'C'); //문자 'C'의 ASCII 코드 값 10진수로 출력
	
	
	//10진수(정수)를 문자(ASCII)로 출력
	printf("%c\n", 65); 
	printf("%c\n", 65 + 1);
	printf("%c\n", 67);

	return 0;

}
